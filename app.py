                           # Comparison Operators
                           
first_No: int = 10
second_No: int = 6

print(first_No == second_No)  # Equal
print(first_No > second_No)   # Greator Than
print(first_No < second_No)   # Less Than
print(first_No >= second_No)  # Greator or Equal to
print(first_No <= second_No)  # Less or Equal to
print(first_No != second_No)  # Not Equal to      



                            
