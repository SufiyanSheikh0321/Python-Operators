                           # Arithmetic Operators 

value1: int = 10
value2: int = 5

print(value1 + value2)  # Addition
print(value1 - value2)  # Subtraction
print(value1 / value2)  # Divison
print(value1 * value2)  # Multiplication
print(value1 ** value2) # Exponent
print(value1 // value2) # Floor Division
print(value1 % value2)  # Modulus
 
 
